        mailSender.setPort(587);

        mailSender.setUsername("loihv.23ite@vku.udn.vn");
        mailSender.setPassword("uebcklvodqkwdtkv"); // Dùng app password của Gmail

        Properties props = mailSender.getJavaMailProperties();
